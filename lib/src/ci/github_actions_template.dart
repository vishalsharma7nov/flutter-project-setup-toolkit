/// Generates GitHub Actions workflow YAML files for the given spec and project config.
Map<String, String> generateGitHubWorkflowFiles({
  required CiWorkflowSpec spec,
  required ToolkitConfig config,
}) {
  final merged = _mergeSpecWithConfig(spec, config);
  if (merged.pipelineMode == CiPipelineMode.split &&
      merged.hasCiJobs &&
      merged.hasReleaseJobs) {
    return {
      CiWorkflowPaths.ciWorkflow: _generateCiWorkflow(merged, config),
      CiWorkflowPaths.releaseWorkflow:
          _generateReleaseWorkflow(merged, config),
    };
  }
  if (merged.pipelineMode == CiPipelineMode.split && merged.hasCiJobs) {
    return {CiWorkflowPaths.ciWorkflow: _generateCiWorkflow(merged, config)};
  }
  return {
    CiWorkflowPaths.releaseWorkflow: _generateUnifiedWorkflow(merged, config),
  };
}

/// Backward-compatible single-file generator used by setup wizard.
String generateGitHubActionsWorkflow({
  required ToolkitConfig config,
  String toolkitPackage = 'flutter_project_setup_toolkit',
  CiWorkflowSpec? spec,
}) {
  final effective = spec ??
      CiWorkflowSpec.fromPreset(CiWorkflowPreset.release).copyWith(
        toolkitPackage: toolkitPackage,
        defaultEnv: config.defaultEnvironment ?? 'dev',
        environmentNames: config.environments.keys.toList(),
      );
  final files = generateGitHubWorkflowFiles(spec: effective, config: config);
  return files.values.first;
}

